package cn.com.tw.gateway.enm;

public enum ApiEnum {
	
	success("00000","success"),
	appidNull("910997","appKey can not be null!"),
	timeNull("910996","timestamp can not be null!"),
	appidError("910998","appid validate error"),
	signNull("911997","sign can not be null!"),
	signError("911998","sign validate error"),
	businessNoNull("911660","businessNo can not be null!");
	;

	private String code;
	
	private String msg;
	
	private ApiEnum(String code, String msg){
		this.code = code;
		this.msg = msg;
	}

	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
	
	
}
