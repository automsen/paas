package cn.com.tw.saas.serv.entity.excel;

public class MeterExcel {

	private String meterAddr;
	
	private String elecCt;
	
	private String elecPt;
	
	private String meterInstallAddr;
	
	private String installRead;
	
	private String message;

	public String getMeterAddr() {
		return meterAddr;
	}

	public void setMeterAddr(String meterAddr) {
		this.meterAddr = meterAddr;
	}

	public String getElecCt() {
		return elecCt;
	}

	public void setElecCt(String elecCt) {
		this.elecCt = elecCt;
	}

	public String getElecPt() {
		return elecPt;
	}

	public void setElecPt(String elecPt) {
		this.elecPt = elecPt;
	}

	public String getMeterInstallAddr() {
		return meterInstallAddr;
	}

	public void setMeterInstallAddr(String meterInstallAddr) {
		this.meterInstallAddr = meterInstallAddr;
	}

	public String getInstallRead() {
		return installRead;
	}

	public void setInstallRead(String installRead) {
		this.installRead = installRead;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
}
