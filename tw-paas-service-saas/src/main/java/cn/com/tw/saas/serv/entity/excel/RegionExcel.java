package cn.com.tw.saas.serv.entity.excel;

public class RegionExcel {

	/**
	 * 楼栋名称
	 */
	private String regionName;
	
	/**
     * 楼栋名编号
     */
    private String regionNumber;
    
    /**
     * 层级
     */
    private String tier;
    
    /**
     * 上级楼栋编号
     */
    private String pRegionNumber;
    /**
     * 错误信息
     */
    private String message;

	public String getRegionName() {
		return regionName;
	}

	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}

	public String getRegionNumber() {
		return regionNumber;
	}

	public void setRegionNumber(String regionNumber) {
		this.regionNumber = regionNumber;
	}

	public String getpRegionNumber() {
		return pRegionNumber;
	}

	public void setpRegionNumber(String pRegionNumber) {
		this.pRegionNumber = pRegionNumber;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getTier() {
		return tier;
	}

	public void setTier(String tier) {
		this.tier = tier;
	}

}
