package cn.com.tw.saas.serv.controller.wechat.vo;

import java.io.Serializable;

/**
 * 每次请求token
 * @author liming
 * 2017年11月9日10:51:23
 */
public class ReqToken implements Serializable{
    /**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
     * 请求类型，APP,NATIVE,WX
     */
	private String reqType;
	
	private String nht_token;
	
	

	public String getReqType() {
		return reqType;
	}
	public void setReqType(String reqType) {
		this.reqType = reqType;
	}
	public String getNht_token() {
		return nht_token;
	}
	public void setNht_token(String nht_token) {
		this.nht_token = nht_token;
	}
	

	
	
	
}
