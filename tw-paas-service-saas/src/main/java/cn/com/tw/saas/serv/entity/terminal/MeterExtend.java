package cn.com.tw.saas.serv.entity.terminal;

import java.math.BigDecimal;

public class MeterExtend extends Meter{

	/**
	 * 所在房间余额
	 */
	private BigDecimal roomBalance;

	/**
	 * 止码
	 */
	private BigDecimal readValue;
	
	/**
	 * 有功功率
	 */
	private BigDecimal activeP;
	
	/**
	 * 通断状态
	 */
	private BigDecimal isOff;

	public BigDecimal getRoomBalance() {
		return roomBalance;
	}

	public void setRoomBalance(BigDecimal roomBalance) {
		this.roomBalance = roomBalance;
	}

	public BigDecimal getReadValue() {
		return readValue;
	}

	public void setReadValue(BigDecimal readValue) {
		this.readValue = readValue;
	}

	public BigDecimal getActiveP() {
		return activeP;
	}

	public void setActiveP(BigDecimal activeP) {
		this.activeP = activeP;
	}

	public BigDecimal getIsOff() {
		return isOff;
	}

	public void setIsOff(BigDecimal isOff) {
		this.isOff = isOff;
	}
}
