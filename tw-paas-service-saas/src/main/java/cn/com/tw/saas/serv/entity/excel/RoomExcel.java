package cn.com.tw.saas.serv.entity.excel;

public class RoomExcel {

	private String roomNumber;
	
    /**
     * 房间类型
     */
    private String roomUse;

    /**
     * 房间名称
     */
    private String roomName;
    /**
     * 面积
     */
    private String area;

    /**
     * 可入住人数
     */
    private String peopleLimit;

	private String message;

	public String getRoomNumber() {
		return roomNumber;
	}

	public void setRoomNumber(String roomNumber) {
		this.roomNumber = roomNumber;
	}

	public String getRoomUse() {
		return roomUse;
	}

	public void setRoomUse(String roomUse) {
		this.roomUse = roomUse;
	}

	public String getRoomName() {
		return roomName;
	}

	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getPeopleLimit() {
		return peopleLimit;
	}

	public void setPeopleLimit(String peopleLimit) {
		this.peopleLimit = peopleLimit;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
