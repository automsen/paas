package cn.com.tw.saas.serv.entity.room;

import java.util.List;

public class RoomTree {

	private String id;
	
	private String pId;
	
	private String name;
	
	private String type;
	
	private boolean open;
	
	private Byte isLeaf;
	
	private Byte isTop;
	
	private int tier;
	
	private List<RoomTree> children;
	
	private String meterCateg;
	
	private String meterAddr;
	
	private Byte isDelete;
	
	private String regionNo;
	
	private String orgId;
	
	private String regionId;

	public RoomTree(){}
	public RoomTree(String id, String pId, String name, String type){
		this.id = id;
		this.pId = pId;
		this.name = name;
		this.type = type;
	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getpId() {
		return pId;
	}

	public void setpId(String pId) {
		this.pId = pId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Byte getIsLeaf() {
		return isLeaf;
	}

	public void setIsLeaf(Byte isLeaf) {
		this.isLeaf = isLeaf;
	}

	public Byte getIsTop() {
		return isTop;
	}

	public void setIsTop(Byte isTop) {
		this.isTop = isTop;
	}

	public int getTier() {
		return tier;
	}

	public void setTier(int tier) {
		this.tier = tier;
	}

	public String getMeterCateg() {
		return meterCateg;
	}

	public void setMeterCateg(String meterCateg) {
		this.meterCateg = meterCateg;
	}

	public String getMeterAddr() {
		return meterAddr;
	}

	public void setMeterAddr(String meterAddr) {
		this.meterAddr = meterAddr;
	}

	public boolean isOpen() {
		return open;
	}

	public void setOpen(boolean open) {
		this.open = open;
	}
	public List<RoomTree> getChildren() {
		return children;
	}
	public void setChildren(List<RoomTree> children) {
		this.children = children;
	}
	public Byte getIsDelete() {
		return isDelete;
	}
	public void setIsDelete(Byte isDelete) {
		this.isDelete = isDelete;
	}
	public String getRegionNo() {
		return regionNo;
	}
	public void setRegionNo(String regionNo) {
		this.regionNo = regionNo;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getRegionId() {
		return regionId;
	}
	public void setRegionId(String regionId) {
		this.regionId = regionId;
	}
	
}
