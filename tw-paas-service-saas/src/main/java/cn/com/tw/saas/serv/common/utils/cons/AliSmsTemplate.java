package cn.com.tw.saas.serv.common.utils.cons;

/**
 * 阿里云短信模板
 * @author admin
 *
 */
public class AliSmsTemplate {
	/**
	 * 签名
	 */
	private String signature;
	/**
	 * 模板编号
	 */
	private String templNum;
	/**
	 * 模板格式
	 */
	private String smsFormat;
	
	public AliSmsTemplate(String signature, String tempNo, String smsFormat) {
		this.signature = signature;
		this.templNum = tempNo;
		this.smsFormat = smsFormat;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public String getTemplNum() {
		return templNum;
	}
	public void setTemplNum(String templNum) {
		this.templNum = templNum;
	}
	public String getSmsFormat() {
		return smsFormat;
	}
	public void setSmsFormat(String smsFormat) {
		this.smsFormat = smsFormat;
	}

}
