package cn.com.tw.common.utils.tools.license;





public class LicenseDate {
	
	private String liceDate;
	
	public LicenseDate(String liceDate){
		
		this.liceDate = liceDate;
		
	}
	
	public boolean isLaterThan(String compDate){
		
		if (liceDate.compareTo(compDate) >= 0){
			return true;
		}
		
		return false;
	}
	
	public String getDate(){
		return liceDate;
	}

}
