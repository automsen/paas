package cn.com.tw.paas.monit.common.utils.cons;

public enum ApiEnum {
	
	success("000000","success"),
	appidNull("010997","appKey can not be null!"),
	timeNull("010996","timestamp can not be null!"),
	appidError("010998","appid validate error"),
	signNull("011997","sign can not be null!"),
	signError("011998","sign validate error"),
	businessNoNull("011660","businessNo can not be null!");
	;

	private String code;
	
	private String msg;
	
	private ApiEnum(String code, String msg){
		this.code = code;
		this.msg = msg;
	}

	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
	
	
}
