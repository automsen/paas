package cn.com.tw.paas.monit.entity.db.base;

import java.io.Serializable;

public class CmdResult  implements Serializable{

	private static final long serialVersionUID = -5156579023772601137L;
	private String cmdId;
	
	/**
	 *  系统生成的业务号
	 */
	private String business_no;
	
	/**
	 *  请求过来的业务号
	 */
	private String out_trance_no;
	
	private String cmdName;
	
	private String meterAddr;
	
	private String errorMsg;
	
	private String success;
	
	

	public String getCmdId() {
		return cmdId;
	}

	public void setCmdId(String cmdId) {
		this.cmdId = cmdId;
	}




	public String getMeterAddr() {
		return meterAddr;
	}

	public void setMeterAddr(String meterAddr) {
		this.meterAddr = meterAddr;
	}

	public String getErrorMsg() {
		return errorMsg;
	}

	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}

	public String getCmdName() {
		return cmdName;
	}

	public void setCmdName(String cmdName) {
		this.cmdName = cmdName;
	}

	public String getBusiness_no() {
		return business_no;
	}

	public void setBusiness_no(String business_no) {
		this.business_no = business_no;
	}

	public String getOut_trance_no() {
		return out_trance_no;
	}

	public void setOut_trance_no(String out_trance_no) {
		this.out_trance_no = out_trance_no;
	}

	public String getSuccess() {
		return success;
	}

	public void setSuccess(String success) {
		this.success = success;
	}
}
